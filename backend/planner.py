class Planner:
    """
    Rule engine that produces a RoutingPlan from an instruction and context.

    Priority order (first match wins):
      1.  Ingest signal      — deterministic keyword/context check
      1b. Diff-only update   — deterministic keyword check + graph-stem
                               resolution; only reached when P1 didn't match
      2.  Memory command     — deterministic keyword check
      3c. Graph-query        — structural link lookup; wins over web_search
                               but defers to file_op/url_fetch (inline guard)
      3.  Tool signal        — deterministic keyword check
      3b. Factual query      — keyword + corpus miss → web_search
      4.  Corpus signal      — deterministic score threshold check
      5.  Episodic relevance — single bounded inference call (added in 3.3)
      -.  Tool-fallback classifier — feature-flagged, shadow-first bounded
          inference call; only reached when P1–P5 all found no match
          (§ P6-fallthrough tool-need classifier). Off by default.
      6.  Direct answer      — fallback (added in 3.4)

    The Planner never calls agents, never writes to the database,
    and never assembles a prompt for the final answer.

    Parameters
    ----------
    runtime :
        RuntimeClient. Used for the Priority 5 inference call, and — only
        when LOCALIST_TOOL_FALLBACK_CLASSIFIER is "shadow" or "active" —
        the P6-fallthrough tool-need classifier's bounded inference call.
        Not used in Priority 1, 2, 4, or 6, and in Priority 3 only for
        negative-filter conflict resolution (2026-07-16 — see
        _resolve_negative_filter_conflict, reached from
        _semantic_search_intent when a _SEARCH_NEGATIVE_FILTER /
        _RESEARCH_NEGATIVE_FILTER phrase matches AND a gated group's score
        clears its own threshold anyway) — never for the base P3 keyword/
        semantic routing decision itself, and validated empirically before
        being wired in (diagnostics/reports/
        negative_filter_tiebreak_assessment_2026-07-16.md: 11/11 accuracy
        on the operationally-reachable subset).
    memory_manager :
        Optional MemoryManager. Required for Priority 4 corpus scoring.
        When absent, Priority 4 is skipped (no corpus to query).
    embedding_model_name :
        Name of the embedding model actually producing embed_fn's vectors
        (e.g. "mlx-community/embeddinggemma-300m-4bit"), or None when no
        embedding source is configured at all (keyword-only mode — embed_fn
        is also None in that case, so semantic scoring is already
        short-circuited). When set and it does not match
        _TUNED_EMBEDDING_MODEL, semantic search-intent gating is disabled
        for this Planner instance — see the module-level comment above
        _TUNED_EMBEDDING_MODEL for why cosine-similarity thresholds don't
        transfer across embedding models.
    """

    # Class-level aliases so callers can access via instance (e.g. p._WEB_SEARCH_KEYWORDS)
    _WEB_SEARCH_KEYWORDS:    frozenset[str]    = _WEB_SEARCH_KEYWORDS
    _FILE_OP_KEYWORDS:       frozenset[str]    = _FILE_OP_KEYWORDS
    _CHART_KEYWORDS:         frozenset[str]    = _CHART_KEYWORDS
    _GATE1_ENV_VAR:          str               = _GATE1_ENV_VAR
    _SLASH_COMMAND_TOOL_MAP: dict[str, str]    = _SLASH_COMMAND_TOOL_MAP

    def __init__(
        self,
        runtime:              Any,
        memory_manager:       "MemoryManager | None" = None,
        embed_fn:             Callable[[str], list[float]] | None = None,
        embedding_model_name: str | None = None,
    ) -> None:
        self._runtime        = runtime
        self._memory_manager = memory_manager
        self._embed_fn       = embed_fn

        # Guard against silently-invalid semantic gating (docs/architecture/
        # 16-runtime-backend-layer.md §16.4): every threshold in this file
        # was tuned against _TUNED_EMBEDDING_MODEL's cosine geometry. A
        # different active embedding model means those thresholds have no
        # validated meaning, so gating is disabled rather than left to
        # silently over/under-fire. None (no embedding source configured at
        # all) is not a mismatch — embed_fn is already None in that case,
        # which already short-circuits semantic scoring.
        self._embedding_model_name     = embedding_model_name
        self._semantic_gating_disabled = False
        if embedding_model_name is not None and embedding_model_name != _TUNED_EMBEDDING_MODEL:
            logger.warning(
                "Planner: active embedding model %r does not match the model "
                "semantic-gating thresholds were tuned against (%r) — "
                "disabling semantic search-intent gating for this session. "
                "The thresholds in _SEMANTIC_GATE_THRESHOLDS / "
                "_RESEARCH_INTENT_THRESHOLD have no validated meaning against "
                "embeddings from a different model.",
                embedding_model_name, _TUNED_EMBEDDING_MODEL,
            )
            self._semantic_gating_disabled = True

        # Session state for Priority 5 caching (§4.3)
        # _episodic_injected: True once episodic bullets have been injected
        #   this session; causes all further Priority 5 checks to return True
        #   without an inference call (relevance assumed to persist).
        # _episodic_cache_pairs: parallel list of (embedding, result)
        #   pairs used for cosine similarity lookup, since dict keys cannot
        #   be float lists.
        self._episodic_injected: bool = False
        self._episodic_cache_pairs: list[tuple[list[float], bool]] = []

        # Gate 1 state for the P6-fallthrough tool-need classifier — the
        # tools_to_call resolved by the most recently completed route() call.
        # Updated automatically at the end of every route() invocation (see
        # record_tools_fired()), mirroring the _episodic_injected pattern:
        # Planner is a single long-lived instance for the process, so this
        # is in-memory only, not persisted across restarts.
        self._last_turn_tools_fired: list[str] = []

        # Diagnostic Slot 1 — flat list of (group_name, vector) pairs, one per
        # template string across all groups in _SEARCH_INTENT_TEMPLATES.
        # Only populated when embed_fn is available; left empty on failure so
        # _semantic_search_intent() safely returns None without logging noise.
        self._template_embeddings: list[tuple[str, list[float]]] = []
        if embed_fn is not None:
            try:
                for group_name, phrases in _SEARCH_INTENT_TEMPLATES.items():
                    for phrase in phrases:
                        vec = embed_fn(phrase)
                        self._template_embeddings.append((group_name, vec))
                logger.debug(
                    "Planner: pre-embedded %d search-intent templates across %d groups.",
                    len(self._template_embeddings), len(_SEARCH_INTENT_TEMPLATES),
                )
            except Exception as exc:  # noqa: BLE001
                logger.warning(
                    "Planner: failed to embed search-intent templates — "
                    "semantic search-intent check will be skipped. Error: %s", exc,
                )
                self._template_embeddings = []

    # -----------------------------------------------------------------------
    # Public API
    # -----------------------------------------------------------------------

    def mark_episodic_injected(self) -> None:
        """
        Signal that episodic bullets were injected into the prompt this session.

        After this is called, Priority 5 will return fetch_episodic=True for
        all subsequent instructions in the same session without making another
        inference call. Relevance is assumed to persist within a session.

        Called by ControllerAgent after a successful episodic retrieval and
        prompt injection.
        """
        self._episodic_injected = True
        logger.debug("Planner: episodic_injected flag set for this session.")

    def record_tools_fired(self, tools: list[str]) -> None:
        """
        Record the tools_to_call resolved for the turn that just finished
        routing, so the next route() call's Gate 1 check (P6-fallthrough
        tool-need classifier) can see it deterministically.

        Called automatically by route() after every turn — this is not
        something callers need to invoke manually in production. It is
        exposed publicly so tests can seed a "prior turn" fixture directly.
        """
        self._last_turn_tools_fired = list(tools)
        logger.debug(
            "Planner: record_tools_fired — last_turn_tools_fired=%r.",
            self._last_turn_tools_fired,
        )

    def _detect_compound(
        self,
        lowered: str,
        context: dict[str, Any],
    ) -> RoutingPlan | None:
        """
        Detect compound instructions that trigger multiple priority conditions
        simultaneously and require special resolution (§4.5).

        Currently handles one compound case:

        Tool + Ingest
        -------------
        Condition: instruction matches both Priority 1 (ingest) AND Priority 3
        (web search tool).
        Resolution: wiki_agent with tools_to_call=["web_search"], compound=True.
        The ControllerAgent will execute the web_search tool first and pass
        the result as context to WikiAgent.

        Returns a RoutingPlan if a compound pattern is detected, or None if
        the instruction should proceed through normal priority evaluation.
        """
        has_ingest = (
            "raw_path" in context
            or any(kw in lowered for kw in _INGEST_KEYWORDS)
        )
        has_web_search = bool(self._any_whole_word(_WEB_SEARCH_KEYWORDS, lowered))

        if has_ingest and has_web_search:
            logger.debug(
                "Planner: compound detected — Tool + Ingest "
                "(ingest=%s, web_search=%s).",
                has_ingest, has_web_search,
            )
            return RoutingPlan(
                agent          = "wiki_agent",
                fetch_episodic = False,
                fetch_rag      = False,
                tools_to_call  = ["web_search"],
                compound       = True,
                tool_signal_source = "keyword",
            )

        return None

    def _priority0_slash_command(self, lowered: str) -> RoutingPlan | None:
        """
        Priority 0 — explicit slash-command tool bypass.

        A leading "/chart" or "/research" token (case-insensitive, either
        the entire instruction or followed by whitespace) forces that tool
        directly, short-circuiting every other priority — including P3's
        _CHART_KEYWORDS keyword match and the research_intent semantic-
        upgrade threshold / LOCALIST_RESEARCH_LOOP_ENABLED gate that
        normally has to be on for "research" to ever enter tools_to_call at
        all (§18.2). Runs before _detect_compound() and every other
        priority evaluation, so it wins even on an instruction that would
        also have matched further down.

        Only a LEADING token counts: a mid-sentence occurrence ("what's a
        good site to /chart my finances") does not match.

        The leading slash token is deliberately NOT stripped from the
        instruction text passed down to tools_to_call's dispatch — this
        RoutingPlan carries only the tool name; instruction stripping is
        left as a separate concern (see the Claude Code prompt that
        introduced this feature).

        Returns a RoutingPlan or None if no match.
        """
        lowered_stripped = lowered.strip()
        for command, tool_name in _SLASH_COMMAND_TOOL_MAP.items():
            if lowered_stripped == command or lowered_stripped.startswith(command + " "):
                logger.debug(
                    "Planner: Priority 0 matched — slash command %r forcing "
                    "tool %r.", command, tool_name,
                )
                return RoutingPlan(
                    agent          = "conversational_agent",
                    fetch_episodic = False,
                    fetch_rag      = False,
                    tools_to_call  = [tool_name],
                    compound       = True,
                    priority       = 0,
                    tool_signal_source = "slash_command",
                )
        return None

    def route(
        self,
        instruction: str,
        context:     dict[str, Any],
    ) -> RoutingPlan:
        """
        Evaluate priorities 1–6 in order and return the first matching plan.

        Thin wrapper around _route_impl(): records the resolved plan's
        tools_to_call after every call (record_tools_fired()) so the next
        turn's P6-fallthrough tool-need classifier Gate 1 check has a
        deterministic, in-memory view of "did the immediately preceding
        turn already fire a tool" — no new dependency, no context/DB
        plumbing required.

        Parameters
        ----------
        instruction :
            The raw user instruction string.
        context :
            The task context dict (may contain raw_path, wiki_dir, etc.).

        Returns
        -------
        RoutingPlan
            Never raises. Guaranteed to return a valid plan.
        """
        plan = self._route_impl(instruction, context)
        self.record_tools_fired(plan.tools_to_call)
        return plan

    def _route_impl(
        self,
        instruction: str,
        context:     dict[str, Any],
    ) -> RoutingPlan:
        """Priority evaluation body for route() — see route() for the public contract."""
        lowered = instruction.lower()

        # Priority 0 — explicit slash-command tool bypass. Must run before
        # compound detection and every other priority so a leading
        # "/chart"/"/research" always wins, regardless of what P3 (or the
        # research-loop flag) would otherwise decide for the same text.
        plan = self._priority0_slash_command(lowered)
        if plan is not None:
            return plan

        # Compound detection — must run before priority evaluation
        plan = self._detect_compound(lowered, context)
        if plan is not None:
            return plan

        # Priority 1 — Ingest signal
        plan = self._priority1_ingest(lowered, context)
        if plan is not None:
            return plan

        # Priority 1b — Diff-only wiki update (must run before Priority 2 so
        # both "write to storage" intents — ingest and diff — stay
        # deterministic and ahead of memory commands; only reached when
        # Priority 1 did not already match)
        plan = self._priority1b_diff(instruction, lowered)
        if plan is not None:
            return plan

        # Priority 2 — Explicit memory command
        plan = self._priority2_memory(lowered)
        if plan is not None:
            return plan

        # Priority 3c — Graph-query (runs before P3 so a graph-query wins over a
        # web_search-only P3 match; P3c's inline guard defers to P3 when
        # file_op/url_fetch signals are present)
        plan = self._priority3c_graph_query(instruction, lowered)
        if plan is not None:
            return plan

        # Priority 3 — Tool signal
        plan = self._priority3_tool(lowered, instruction)
        if plan is not None:
            return plan

        # Priority 3b — Factual query + corpus miss
        plan = self._priority3b_factual(instruction, lowered)
        if plan is not None:
            return plan

        # Priority 4 — Corpus signal
        plan = self._priority4_corpus(lowered, instruction)
        if plan is not None:
            # P4 matched — also run P5 to check episodic relevance.
            # If both match, merge into a compound plan so the controller
            # fetches both RAG context and episodic memory for this turn.
            p5 = self._priority5_episodic(instruction)
            if p5 is not None:
                logger.debug(
                    "Planner: P4+P5 compound — fetch_rag=True fetch_episodic=True."
                )
                plan.fetch_episodic = True
            return plan

        # Priority 5 — Episodic relevance (single bounded inference call)
        plan = self._priority5_episodic(instruction)
        if plan is not None:
            return plan

        # P6-fallthrough tool-need classifier — feature-flagged, shadow-first.
        # Only reached when P1, P2, P3/3b/3c, P4, and P5 have all found no
        # match. Off by default: _maybe_tool_fallback_classifier() returns
        # None immediately without calling _classify_tool_fallback() at all.
        plan = self._maybe_tool_fallback_classifier(instruction, context)
        if plan is not None:
            return plan

        # Priority 6 — Direct answer fallback
        return self._priority6_direct()

    # -----------------------------------------------------------------------
    # Priority 1 — Ingest signal  (§4.2, Priority 1)
    # -----------------------------------------------------------------------

    def _priority1_ingest(
        self,
        lowered: str,
        context: dict[str, Any],
    ) -> RoutingPlan | None:
        """
        Match condition: raw_path key present in context OR ingest keyword
        in lowercased instruction.

        Returns a RoutingPlan routed to wiki_agent, or None if no match.
        fetch_rag and fetch_episodic are explicitly False — ingest is never
        augmented with retrieval.
        """
        has_raw_path = "raw_path" in context
        has_keyword  = any(kw in lowered for kw in _INGEST_KEYWORDS)

        if has_raw_path or has_keyword:
            logger.debug(
                "Planner: Priority 1 matched (raw_path=%s, keyword=%s).",
                has_raw_path, has_keyword,
            )
            return RoutingPlan(
                agent          = "wiki_agent",
                fetch_episodic = False,
                fetch_rag      = False,
                priority       = 1,
            )
        return None

    # -----------------------------------------------------------------------
    # Priority 1b — Diff-only wiki update
    # -----------------------------------------------------------------------

    def _priority1b_diff(
        self,
        instruction: str,
        lowered:     str,
    ) -> RoutingPlan | None:
        """
        Priority 1b — diff-only wiki update ("update page X to reflect Y",
        with no raw file involved). Only reached when Priority 1 (ingest)
        did not already match, so an ingest instruction that happens to
        also contain a diff keyword still takes the ingest path unchanged.

        Resolves the target page name via the same resolve_graph_target()
        three-tier pipeline Priority 3c uses, fed by
        memory_manager.list_graph_node_stems(). On successful resolution,
        routes to wiki_agent with diff_target set. On a keyword match with
        failed/ambiguous resolution (or no MemoryManager to resolve
        against), routes to conversational_agent with no tools/retrieval so
        the model can ask the user which page they mean — the turn is
        claimed either way; it deliberately does NOT fall through to P2+
        on an unresolved target. Both P1 and P1b are "write to storage"
        intents and stay on the same deterministic, early footing ahead of
        P2 memory commands (Michael's decision, 2026-07-09).

        Returns None only when no _DIFF_KEYWORDS lead phrase is present.
        """
        remainder = extract_diff_query(instruction)
        if remainder is None:
            return None

        if self._memory_manager is None:
            logger.debug(
                "Planner: Priority 1b matched keyword but no MemoryManager — "
                "routing to conversational_agent for clarification."
            )
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = False,
                fetch_rag      = False,
                priority       = 1,
            )

        try:
            candidate_stems = self._memory_manager.list_graph_node_stems()
        except Exception as exc:
            logger.warning(
                "Planner: Priority 1b — list_graph_node_stems failed: %s", exc
            )
            candidate_stems = []

        resolved_stem = resolve_graph_target(remainder, candidate_stems)

        if resolved_stem is None:
            logger.debug(
                "Planner: Priority 1b — target resolution failed for %r; "
                "routing to conversational_agent for clarification.",
                remainder,
            )
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = False,
                fetch_rag      = False,
                priority       = 1,
            )

        logger.debug(
            "Planner: Priority 1b matched — diff_target=%r.", resolved_stem
        )
        return RoutingPlan(
            agent          = "wiki_agent",
            fetch_episodic = False,
            fetch_rag      = False,
            priority       = 1,
            diff_target    = resolved_stem,
        )

