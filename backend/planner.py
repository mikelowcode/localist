    # Priority 3 — Tool signal  (§4.2, Priority 3)
    # -----------------------------------------------------------------------

    @staticmethod
    def _any_whole_word(keywords: frozenset[str], text: str) -> str | None:
        """
        Return the first keyword from `keywords` that appears as a whole
        word (or whole phrase) in `text`, or None if no match.

        Multi-word keywords (e.g. "create a file") are matched as a literal
        phrase with word boundaries on each end. Single-word keywords are
        matched with \\b anchors. Matching is case-insensitive; callers
        should pass already-lowercased text.
        """
        for kw in keywords:
            pattern = r"\b" + re.escape(kw) + r"\b"
            if re.search(pattern, text):
                return kw
        return None

    def _semantic_search_intent(
        self, lowered: str
    ) -> tuple[str, float, dict[str, float]] | None:
        """
        Diagnostic-only (Slot [Diagnostic 2]): returns the best-matching
        group, its score, AND the max score per group across all groups —
        so collisions between groups can be inspected directly, not just
        inferred from the argmax.

        Returns (best_group, best_score, all_group_scores) or None under:
        no embed_fn, embedding failure, no group scored above -1, or a
        confirmed negative-filter suppression (see below).

        2026-07-16 redesign (negative-filter conflict resolution): prior to
        this, _SEARCH_NEGATIVE_FILTER / _RESEARCH_NEGATIVE_FILTER were
        checked BEFORE the embedding was computed, so (a) no score was ever
        available to log for a filtered turn, contrary to how this method
        is described elsewhere as running "on every turn", and (b) a filter
        match on one group's known collision (e.g. lookup_request's
        greeting/identity phrases) suppressed ALL groups' gating that turn,
        research_intent included, not just the group the filter targets.

        Scores are now always computed first. The negative filters are
        checked AFTER scoring, and only escalate to a model call
        (_resolve_negative_filter_conflict) when there's a genuine
        conflict — a filter phrase matched AND some gated group's score
        cleared its own threshold anyway. If nothing cleared threshold,
        the filter and the embedding already agree (nothing was going to
        fire) and no call is needed — empirically the common case: see
        diagnostics/reports/negative_filter_tiebreak_assessment_2026-07-16.md
        §3, where only 4-8 of 11 filter-matched test utterances (depending
        on LOCALIST_RESEARCH_LOOP_ENABLED) actually reached a conflict.
        """
        if (
            self._embed_fn is None
            or not self._template_embeddings
            or self._semantic_gating_disabled
        ):
            return None

        try:
            query_vec = self._embed_fn(lowered)
        except Exception as exc:  # noqa: BLE001
            logger.warning(
                "Planner: _semantic_search_intent — embed_fn raised: %s", exc,
            )
            return None

        # Per-group max scores: accumulate highest similarity for each group.
        group_scores: dict[str, float] = {g: -1.0 for g in _SEARCH_INTENT_TEMPLATES}
        for group_name, tmpl_vec in self._template_embeddings:
            score = _cosine_similarity(query_vec, tmpl_vec)
            if score > group_scores[group_name]:
                group_scores[group_name] = score

        best_group = max(group_scores, key=lambda g: group_scores[g])
        best_score = group_scores[best_group]

        if best_score < 0:
            return None

        matched_phrase = next(
            (p for p in _ALL_SEARCH_NEGATIVE_FILTERS if p in lowered), None
        )
        if matched_phrase is not None:
            # research_intent is only a "gated" group worth resolving a
            # conflict over when the research loop is actually enabled —
            # otherwise nothing acts on its score regardless.
            gated_groups = dict(_SEMANTIC_GATE_THRESHOLDS)
            if self._research_loop_enabled():
                gated_groups["research_intent"] = _RESEARCH_INTENT_THRESHOLD

            conflicting = [
                g for g, t in gated_groups.items() if group_scores.get(g, -1.0) >= t
            ]
            if not conflicting:
                # Filter and embedding already agree — nothing was going to
                # fire anyway. No model call needed.
                logger.debug(
                    "Planner: Priority 3 semantic check — negative filter %r "
                    "matched but no group cleared threshold; no conflict.",
                    matched_phrase,
                )
                return None

            if not self._resolve_negative_filter_conflict(lowered, matched_phrase):
                logger.debug(
                    "Planner: Priority 3 semantic check — negative filter %r "
                    "confirmed by tie-break over conflicting group(s) %s; "
                    "suppressing.",
                    matched_phrase, conflicting,
                )
                return None

            logger.debug(
                "Planner: Priority 3 semantic check — negative filter %r "
                "overridden by tie-break for group(s) %s.",
                matched_phrase, conflicting,
            )

        return (best_group, best_score, group_scores)

    def _resolve_negative_filter_conflict(
        self, lowered: str, matched_phrase: str
    ) -> bool:
        """
        Single bounded inference call — the SAME "one classifier call,
        yes/no or single-word answer" shape as the Priority 5
        episodic-relevance check and the P6-fallthrough tool-need
        classifier. Only reached when a negative-filter phrase matched AND
        at least one semantically-gated group cleared its threshold
        anyway; the common case (filter matches, nothing near threshold)
        never gets here, so this should be rare in practice, not a
        per-turn cost — confirmed empirically (not assumed) in
        diagnostics/reports/negative_filter_tiebreak_assessment_2026-07-16.md
        §3: 4/11 (research loop off) to 8/11 (on) filter-matched
        utterances actually reach this call.

        Returns True to override the filter (real lookup intent — let
        best_group/best_score through) or False to confirm the filter's
        suppression (return None from the caller).

        Fails closed: any runtime error, timeout, or unparseable response
        suppresses the match (returns False) — a live failure here should
        not accidentally let a known collision phrase through gating;
        same fail-safe direction _research_loop_enabled() and every other
        gate in this file already defaults to.

        Accuracy validated before wiring this in: 11/11 (100%) on the
        operationally-reachable subset of the 2026-07-16 diagnostic's
        37-utterance test set (0 false positives — a real Category E
        opinion incorrectly let through — and 0 false negatives — a real
        lookup incorrectly suppressed).
        """
        try:
            raw = self._runtime.infer(
                system      = _NEGATIVE_FILTER_TIEBREAK_SYSTEM_PROMPT,
                prompt      = f"Instruction: {lowered}\n\nClassification (lookup/other):",
                max_tokens  = 10,
                temperature = 0.1,
            )
            return raw.strip().lower().startswith("lookup")
        except Exception as exc:
            logger.debug(
                "Planner: negative-filter tie-break failed (%s); suppressing.", exc
            )
            return False

    @staticmethod
    def _research_loop_enabled() -> bool:
        """
        Read LOCALIST_RESEARCH_LOOP_ENABLED from the environment.

        Read at call time (not cached at construction) — same rationale as
        _tool_fallback_mode(): flipping backend/.env shouldn't require
        reconstructing the Planner. Defaults to disabled; unset or any
        value other than "true" (case-insensitive) is "off" — fail-safe to
        zero behavior change (plain web_search) rather than fail-open into
        the loop.
        """
        return os.environ.get(_RESEARCH_LOOP_ENV_VAR, "false").strip().lower() == "true"

    def _priority3_tool(
        self, lowered: str, instruction: str | None = None
    ) -> RoutingPlan | None:
        """
        Match condition: web search keyword, explicit-date pattern, semantic
        search-intent gate, chart keyword, OR file operation keyword present
        in lowercased instruction — see _WEB_SEARCH_KEYWORDS,
        _has_explicit_date(), _SEMANTIC_GATE_THRESHOLDS, and _CHART_KEYWORDS
        respectively; any one alone is sufficient.

        Populates tools_to_call with "web_search", "chart", and/or "file_op"
        as appropriate. Sets compound=True when a tool is scheduled alongside
        a response agent, since the tool must run before the agent call.

        file_op is detected via either a literal _FILE_OP_KEYWORDS hit (read/
        open/create) or a filename-destination phrase from
        _FILE_OP_PATH_PATTERNS ("save it as X.ext", "name it X.ext", "call it
        X.ext") — the latter catches write/save instructions that don't use
        one of the narrow trigger keywords, e.g. "write a haiku about the sea
        and save it as haiku.md". Once file_op is matched, a write/append
        action additionally needs content to write. If that content is
        already present in the instruction (fenced code block, quoted span,
        or "with the content"/"containing"/"that says" phrasing — same
        signal _derive_file_op_content() extracts from at dispatch time),
        behavior is unchanged: "file_op" is added to tools_to_call. If no
        such content is present, the content must be generated by the agent
        first, so "file_op" is deliberately left out of tools_to_call (there
        is nothing to dispatch yet) and file_op_deferred/file_op_path/
        file_op_action are set instead so a future controller step can
        dispatch file_op once generation completes. A bare read action never
        needs content, so it always takes the tools_to_call=["file_op"] path
        regardless of content presence.

        `instruction` (original case) is optional and defaults to `lowered`
        when omitted — every existing direct caller of this method (tests
        included) passes only the lowered string, and content/path
        derivation below tolerates lowered input fine (quotes/fences aren't
        case-sensitive; _derive_file_op_content's phrase pattern and the
        destination patterns are already re.IGNORECASE). Passing the raw
        instruction (as route() does) only improves case fidelity of the
        derived file_op_path/file_op_action in the deferred branch.

        Returns a RoutingPlan or None if no match.
        """
        tools: list[str] = []

        ws_kw = self._any_whole_word(_WEB_SEARCH_KEYWORDS, lowered)
        if ws_kw:
            tools.append("web_search")
            logger.debug(
                "Planner: Priority 3 — web_search signal detected (%r).", ws_kw
            )

        chart_kw = self._any_whole_word(_CHART_KEYWORDS, lowered)
        if chart_kw:
            tools.append("chart")
            logger.debug(
                "Planner: Priority 3 — chart signal detected (%r).", chart_kw
            )

        semantic_result = self._semantic_search_intent(lowered)
        semantic_triggered = False
        fired_groups: list[str] = []
        if semantic_result is not None:
            best_group, best_score, all_scores = semantic_result
            fired_groups = [
                group for group, threshold in _SEMANTIC_GATE_THRESHOLDS.items()
                if all_scores.get(group, 0.0) >= threshold
            ]
            semantic_triggered = bool(fired_groups)
            logger.debug(
                "Planner: Priority 3 — semantic signal best=%r(%.3f) all=%s "
                "gate_fired=%s.",
                best_group, best_score, all_scores, semantic_triggered,
            )

        if _has_explicit_date(lowered) and "web_search" not in tools:
            tools.append("web_search")
            logger.debug(
                "Planner: Priority 3 — web_search signal detected via "
                "explicit-date pattern."
            )

        if semantic_triggered and "web_search" not in tools:
            tools.append("web_search")
            fired_str = ", ".join(
                f"{group}={all_scores[group]:.3f}(>={_SEMANTIC_GATE_THRESHOLDS[group]:.2f})"
                for group in fired_groups
            )
            logger.debug(
                "Planner: Priority 3 — web_search signal detected via semantic "
                "gate (fired=[%s]).", fired_str,
            )

        # research_intent upgrade: decide web_search vs research using the
        # SAME semantic_result already computed above; no second embed call.
        # research_intent only ever decides which tool variant fires, given
        # web_search already fired for some other reason — it is not itself
        # a gate on whether a tool fires at all (see the module-level note
        # above _RESEARCH_LOOP_ENV_VAR), which is why it lives outside
        # _SEMANTIC_GATE_THRESHOLDS rather than as an entry in it.
        if (
            "web_search" in tools
            and semantic_result is not None
            and self._research_loop_enabled()
        ):
            _, _, all_scores = semantic_result
            research_score = all_scores.get("research_intent", -1.0)
            if research_score >= _RESEARCH_INTENT_THRESHOLD:
                tools[tools.index("web_search")] = "research"
                logger.debug(
                    "Planner: Priority 3 — upgraded web_search to research "
                    "(research_intent=%.3f >= %.2f).",
                    research_score, _RESEARCH_INTENT_THRESHOLD,
                )

        raw = instruction if instruction is not None else lowered

        fo_kw = self._any_whole_word(_FILE_OP_KEYWORDS, lowered)
        fo_dest_match: str | None = None
        if not fo_kw:
            for pattern in _FILE_OP_PATH_PATTERNS:
                if pattern.search(raw):
                    fo_dest_match = pattern.pattern
                    break

        file_op_deferred = False
        file_op_path:   str | None = None
        file_op_action: str | None = None

        if fo_kw or fo_dest_match:
            action = _derive_file_op_action(raw)
            content_needed  = action in ("write", "append")
            content_present = bool(_derive_file_op_content(raw))

            if not content_needed or content_present:
                tools.append("file_op")
                logger.debug(
                    "Planner: Priority 3 — file_op signal detected "
                    "(kw=%r dest_match=%r action=%r content_present=%s).",
                    fo_kw, fo_dest_match, action, content_present,
                )
            else:
                file_op_deferred = True
                file_op_path   = _derive_file_op_path(raw)
                file_op_action = action
                logger.debug(
                    "Planner: Priority 3 — file_op signal detected but "
                    "content is generation-required (kw=%r dest_match=%r "
                    "action=%r path=%r); deferring dispatch until content "
                    "exists.",
                    fo_kw, fo_dest_match, action, file_op_path,
                )

        if self._any_whole_word(_FETCH_KEYWORDS, lowered) or re.search(
            r"https?://", lowered
        ):
            tools.append("url_fetch")
            logger.debug("Planner: Priority 3 — url_fetch signal detected.")

        if tools or file_op_deferred:
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = False,
                fetch_rag      = False,
                tools_to_call  = tools,
                compound       = True,
                priority       = 3,
                tool_signal_source = "keyword",
                file_op_deferred = file_op_deferred,
                file_op_path     = file_op_path,
                file_op_action   = file_op_action,
            )
        return None

    # -----------------------------------------------------------------------
    # Priority 3c — Graph-query  (structural link lookup)
    # -----------------------------------------------------------------------

    def _priority3c_graph_query(
        self,
        instruction: str,
        lowered:     str,
    ) -> RoutingPlan | None:
        """
        Priority 3c — Graph-query (structural "what links to X" /
        "what does X link to" lookup).

        Wins over web_search/P3b/P4a/P4 but loses to P1/P2 (handled by
        route()'s call order, not here) and to P3's file_op/url_fetch
        signals specifically (handled by the inline guard below — there is
        no standalone pre-check method for those two signals; the module-
        level note above _priority3_tool() explains the design rationale —
        do NOT remove that comment when editing nearby code).

        Returns None (deferring to normal priority evaluation) if:
          - file_op or url_fetch keywords are present (inline guard), OR
          - no MemoryManager is available, OR
          - no extraction pattern matches the instruction, OR
          - name resolution fails at all tiers (zero or ambiguous matches).

        On success, returns a RoutingPlan with graph_query set and
        fetch_rag/fetch_episodic all False — graph-query turns are deliberately
        pure/minimal, never combined with RAG or episodic context (Phase C design).
        """
        # 1. Inline file_op/url_fetch guard.
        #    Intentionally duplicates _priority3_tool()'s file_op/url_fetch
        #    detection — this duplication is deliberate (no standalone
        #    pre-check exists for those two signals without modifying
        #    _priority3_tool()). The _FILE_OP_PATH_PATTERNS check mirrors
        #    _priority3_tool()'s destination-phrase signal ("save it as
        #    X.ext" etc.) so P3c still defers to P3 for file_op instructions
        #    that only match via that signal (no _FILE_OP_KEYWORDS hit) —
        #    keep both in sync if either changes.
        #    _WEB_SEARCH_KEYWORDS is NOT guarded here: P3c must win over a
        #    web_search-only P3 match (hence its placement before P3 in route()).
        if (
            self._any_whole_word(_FILE_OP_KEYWORDS, lowered)
            or any(p.search(instruction) for p in _FILE_OP_PATH_PATTERNS)
            or self._any_whole_word(_FETCH_KEYWORDS, lowered)
            or re.search(r"https?://", lowered)
        ):
            return None

        # 2. MemoryManager availability check (same pattern as P3b/P4).
        if self._memory_manager is None:
            logger.debug("Planner: Priority 3c skipped — no MemoryManager.")
            return None

        # 3. Extraction.
        extracted = extract_graph_query(instruction)
        if extracted is None:
            return None
        direction, remainder = extracted

        # 4. Fetch candidate stems from the live graph_nodes table.
        try:
            candidate_stems = self._memory_manager.list_graph_node_stems()
        except Exception as exc:
            logger.warning(
                "Planner: Priority 3c — list_graph_node_stems failed: %s", exc
            )
            return None

        # 5. Name resolution (three-tier deterministic pipeline).
        resolved_stem = resolve_graph_target(remainder, candidate_stems)
        if resolved_stem is None:
            logger.debug(
                "Planner: Priority 3c — name resolution failed for %r.", remainder
            )
            return None

        # 6. Node id lookup.  Should always succeed since resolved_stem came
        #    from the same graph_nodes table, but guard defensively in case of
        #    a race between step 4 and step 6 in a concurrent-write scenario.
        try:
            node = self._memory_manager.resolve_node_by_stem(resolved_stem)
        except Exception as exc:
            logger.warning(
                "Planner: Priority 3c — resolve_node_by_stem failed: %s", exc
            )
            return None

        if node is None:
            logger.warning(
                "Planner: Priority 3c — resolve_node_by_stem returned None for "
                "%r (race condition or DB inconsistency).", resolved_stem
            )
            return None

        node_id = node["id"]
        logger.debug(
            "Planner: Priority 3c matched — direction=%r stem=%r node_id=%d.",
            direction, resolved_stem, node_id,
        )

        # 7. Build the plan.
        return RoutingPlan(
            agent          = "conversational_agent",
            fetch_episodic = False,
            fetch_rag      = False,
            compound       = False,
            priority       = 3,
            graph_query    = (direction, node_id, resolved_stem),
        )

    # -----------------------------------------------------------------------
    # Priority 3b — Factual query + corpus miss
    # -----------------------------------------------------------------------

    def _priority3b_factual(self, instruction: str, lowered: str) -> RoutingPlan | None:
        """
        Match condition: instruction contains a factual query keyword AND
        corpus query returns no result above _CORPUS_SCORE_THRESHOLD.

        When no MemoryManager is available, this priority is skipped entirely
        (returns None) — corpus cannot be checked.

        Returns a RoutingPlan with tools_to_call=["web_search"], or None.
        """
        if not any(kw in lowered for kw in _FACTUAL_QUERY_KEYWORDS):
            return None

        if self._memory_manager is None:
            logger.debug("Planner: Priority 3b skipped — no MemoryManager.")
            return None

        try:
            results = self._memory_manager.query_corpus(
                instruction, max_results=1
            )
        except Exception as exc:
            logger.warning("Planner: Priority 3b corpus check failed: %s", exc)
            results = []

        top_score = results[0].relevance_score if results else 0.0

        if top_score >= _CORPUS_SCORE_THRESHOLD:
            logger.debug(
                "Planner: Priority 3b — corpus hit (score=%.3f), "
                "deferring to Priority 4.", top_score,
            )
            return None

        logger.debug(
            "Planner: Priority 3b matched — factual keyword, "
            "corpus miss (score=%.3f), scheduling web_search.", top_score,
        )
        return RoutingPlan(
            agent          = "conversational_agent",
            fetch_episodic = False,
            fetch_rag      = False,
            tools_to_call  = ["web_search"],
            compound       = True,
            priority       = 3,
            tool_signal_source = "keyword",
        )

    # -----------------------------------------------------------------------
    # Priority 4 — Corpus signal  (§4.2, Priority 4)
    # -----------------------------------------------------------------------

    def _priority4_corpus(self, lowered: str, instruction: str) -> RoutingPlan | None:
        """
        Match condition (either sufficient):
          A) instruction contains an explicit wiki/vault trigger keyword, OR
          B) MemoryManager is available AND query_corpus() returns a top result
             with relevance_score >= _CORPUS_SCORE_THRESHOLD.

        Path A keeps routing deterministic for explicit wiki requests.
        Path B restores score-based RAG injection for natural-language corpus
        queries that lack a trigger keyword (e.g. "summarize the LORA Master
        Project Outline").

        Returns a RoutingPlan with fetch_rag=True, or None if no match.
        """
        # Path A — explicit keyword trigger
        matched_kw = next(
            (kw for kw in _WIKI_QUERY_KEYWORDS if kw in lowered), None
        )
        if matched_kw is not None:
            logger.debug(
                "Planner: Priority 4 matched via keyword (%r).", matched_kw
            )
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = True,
                fetch_rag      = True,
                compound       = False,
                priority       = 4,
            )

        # Path B — corpus score threshold
        if self._memory_manager is None:
            logger.debug("Planner: Priority 4 Path B skipped — no MemoryManager.")
            return None

        try:
            results = self._memory_manager.query_corpus(
                instruction, max_results=1
            )
        except Exception as exc:
            logger.warning("Planner: Priority 4 corpus check failed: %s", exc)
            return None

        top_score = results[0].relevance_score if results else 0.0

        if top_score >= _CORPUS_SCORE_THRESHOLD:
            logger.debug(
                "Planner: Priority 4 matched via corpus score (%.3f >= %.3f).",
                top_score, _CORPUS_SCORE_THRESHOLD,
            )
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = False,
                fetch_rag      = True,
                compound       = False,
                priority       = 4,
            )

        logger.debug(
            "Planner: Priority 4 — corpus miss (top_score=%.3f).", top_score
        )
        return None

    # -----------------------------------------------------------------------
    # Priority 5 — Episodic relevance  (§4.2, §4.3)
    # -----------------------------------------------------------------------

    def _priority5_episodic(self, instruction: str) -> RoutingPlan | None:
        """
        Priority 5 — Episodic relevance (§4.2, §4.3).

        Replaced inference call with deterministic keyword check for
        Gemma 4B compatibility. The model requires max_tokens=300 to
        produce binary classifier output, making inference-based routing
        too expensive for a per-turn call.

        Returns a RoutingPlan with fetch_episodic=True if the instruction
        contains episodic relevance signals, or None if not.

        Session flag caching is preserved: once episodic bullets have been
        injected this session, all subsequent turns return fetch_episodic=True
        without keyword evaluation.
        """
        # Cache check: session-level flag.
        # When set, we skip the (previously: inference) call and go straight
        # to keyword evaluation — but we do NOT return True unconditionally.
        # A turn with no episodic keyword still returns None so P6 or P4 can
        # handle it correctly.
        _skip_inference = self._episodic_injected
        if _skip_inference:
            logger.debug(
                "Planner: Priority 5 — episodic_injected=True; "
                "skipping inference, proceeding to keyword check."
            )

        # Deterministic keyword check
        _EPISODIC_KEYWORDS: frozenset[str] = frozenset({
            "preference", "preferences", "remember", "remembered",
            "you know about me", "what do you know",
            "decision", "decisions", "decided",
            "correction", "corrections", "wrong",
            "workflow", "workflows",
            "last time", "previously", "before",
            "my project", "my setup", "my environment",
            # Personal reference signals (unambiguous — always fetch episodic)
            "my name", "do you remember", "who am i",
            "what do you know about me", "my preference",
            "what did i tell you", "what have i told you",
        })

        lowered = instruction.lower()
        matched = next(
            (kw for kw in _EPISODIC_KEYWORDS if kw in lowered), None
        )

        if matched:
            logger.debug(
                "Planner: Priority 5 — episodic keyword matched %r → "
                "fetch_episodic=True.", matched
            )
            return RoutingPlan(
                agent          = "conversational_agent",
                fetch_episodic = True,
                fetch_rag      = False,
                priority       = 5,
            )

        logger.debug("Planner: Priority 5 — no episodic keyword matched.")
        return None

    # -----------------------------------------------------------------------
    # P6-fallthrough tool-need classifier  (feature-flagged, shadow-first)
    # -----------------------------------------------------------------------

    @staticmethod
    def _tool_fallback_mode() -> str:
        """
        Read LOCALIST_TOOL_FALLBACK_CLASSIFIER from the environment.

        Read at call time (not cached) so tests and ops tooling can flip
        modes without reconstructing the Planner. Any value other than
        "off" / "shadow" / "active" (including unset) is treated as "off" —
        fail-safe to zero behavior change.
        """
        mode = os.environ.get(_TOOL_FALLBACK_ENV_VAR, "off").strip().lower()
        return mode if mode in _TOOL_FALLBACK_VALID_MODES else "off"

    def _classify_tool_fallback(
        self,
        instruction: str,
        context:     dict[str, Any],
    ) -> str | None:
        """
        Bounded single-inference-call classifier: "is a tool needed for this
        instruction, and which one?" Called only when P1, P2, P3/3b/3c, and
        P4 have all found no match for this turn (i.e. routing is about to
        fall through past Priority 5 toward Priority 6) AND the
        LOCALIST_TOOL_FALLBACK_CLASSIFIER flag is "shadow" or "active" (see
        _maybe_tool_fallback_classifier — when the flag is "off", this
        method is never called at all).

        Two hard gates run before any model call:

          Gate 1 — deterministic, not a model judgment: skip if the most
          recently completed turn's tools_to_call was non-empty (tracked in
          self._last_turn_tools_fired, updated by route()/record_tools_fired()
          after every turn). This makes the "tool need was already resolved
          last turn" failure mode structurally impossible rather than
          something the model is asked to avoid.

          Gate 2 — dependency check: skip if memory_manager or runtime is
          unavailable, the same fail-open-to-existing-behavior pattern
          Priority 4/5 already use when their dependencies are absent.

        The model is asked for exactly one word — one of web_search,
        file_op, url_fetch, or none — and nothing else. Parsed via strict
        exact-match (after .strip().lower()) against that known set; any
        other output (empty, prose, garbage) parses to None, same as "none".
        No arguments are requested from or derived from the model's output;
        query/path/url resolution for whichever tool name is returned stays
        entirely with the existing deterministic derivation logic
        (MCPToolDispatcher), exactly as it already runs today for P3/P3b
        tools_to_call matches.

        Returns the parsed tool name, or None (no tool needed, gate skip,
        or any parse/inference failure). Never raises.
        """
        if os.environ.get(self._GATE1_ENV_VAR, "on").strip().lower() != "off":
            if self._last_turn_tools_fired:
                logger.info(
                    "Planner: tool-fallback classifier — instruction=%r gate1_pass=False "
                    "(prior turn tools_fired=%r); skipping.",
                    instruction, self._last_turn_tools_fired,
                )
                return None
        else:
            logger.info(
                "Planner: tool-fallback classifier — instruction=%r Gate 1 disabled via %s=off "
                "(prior turn tools_fired=%r, would have gated if enabled).",
                instruction, self._GATE1_ENV_VAR, self._last_turn_tools_fired,
            )

        if self._memory_manager is None or self._runtime is None:
            logger.info(
                "Planner: tool-fallback classifier — instruction=%r gate1_pass=True "
                "gate2_pass=False (memory_manager_present=%s, runtime_present=%s); "
                "skipping.",
                instruction,
                self._memory_manager is not None,
                self._runtime is not None,
            )
            return None

        prompt = (
            f"User instruction: {instruction}\n\n"
            "Does answering this require calling an external tool? Respond "
            "with exactly one word: web_search, file_op, url_fetch, or none."
        )

        try:
            raw = self._runtime.infer(
                prompt,
                system      = _TOOL_FALLBACK_SYSTEM_PROMPT,
                max_tokens  = 8,
                temperature = 0.0,
            )
        except Exception as exc:  # noqa: BLE001
            logger.warning(
                "Planner: tool-fallback classifier — instruction=%r gate1_pass=True "
                "gate2_pass=True runtime.infer raised: %s; treating as None.",
                instruction, exc,
            )
            return None

        parsed = raw.strip().lower()
        tool = parsed if parsed in _TOOL_FALLBACK_TOOL_NAMES else None

        logger.info(
            "Planner: tool-fallback classifier — instruction=%r gate1_pass=True "
            "gate2_pass=True raw_output=%r parsed_tool=%r.",
            instruction, raw, tool,
        )
        return tool

    def _maybe_tool_fallback_classifier(
        self,
        instruction: str,
        context:     dict[str, Any],
    ) -> RoutingPlan | None:
        """
        Feature-flag wrapper around _classify_tool_fallback().

          off    (default) — returns None immediately; _classify_tool_fallback
                              is never called. Zero behavior change, zero
                              latency change.
          shadow            — _classify_tool_fallback runs (and logs), but
                              the result is discarded here: this method
                              still returns None, so route() proceeds to
                              _priority6_direct() exactly as it would with
                              the flag off. For observing real traffic
                              before the classifier is trusted to touch
                              routing.
          active            — a parsed tool name sets tools_to_call on a new
                              RoutingPlan as P3 would have, tagged
                              tool_signal_source="classifier_fallback" so
                              it is never confused with a keyword match in
                              per-turn metadata. A parsed None (no tool
                              needed) returns None here too, deferring to
                              _priority6_direct().
        """
        mode = self._tool_fallback_mode()
        if mode == "off":
            return None

        tool = self._classify_tool_fallback(instruction, context)

        if mode == "shadow":
            logger.info(
                "Planner: tool-fallback classifier — shadow mode, parsed_tool=%r "
                "discarded; RoutingPlan unaffected.", tool,
            )
            return None

        # mode == "active"
        if tool is None:
            return None

        logger.info(
            "Planner: tool-fallback classifier — active mode, tools_to_call=[%r] "
            "via classifier_fallback.", tool,
        )
        return RoutingPlan(
            agent              = "conversational_agent",
            fetch_episodic     = False,
            fetch_rag          = False,
            tools_to_call      = [tool],
            compound           = True,
            priority           = 6,
            tool_signal_source = "classifier_fallback",
        )

    # -----------------------------------------------------------------------
    # Priority 6 — Direct answer fallback  (§4.2, Priority 6)
    # -----------------------------------------------------------------------

    def _priority6_direct(self) -> RoutingPlan:
        """
        Priority 6 — Direct answer fallback (§4.2, Priority 6).

        Reached only when no prior priority matched. Routes to
        ConversationalAgent with no retrieval — the model answers from
        its own weights plus working memory (slots 1–3 only).
        """
        logger.debug("Planner: Priority 6 — direct answer fallback.")
        return RoutingPlan(
            agent          = "conversational_agent",
            fetch_episodic = False,
            fetch_rag      = False,
            priority       = 6,
        )
