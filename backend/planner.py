@dataclass
class RoutingPlan:
    """
    The output of the Planner. Consumed by ControllerAgent.handle_task().

    Fields
    ------
    agent :
        "wiki_agent" | "conversational_agent"
    fetch_episodic :
        True → retrieve from episodes table and inject into slot 4.
    fetch_rag :
        True → query_corpus() before responding; results populate slot 5.
    tools_to_call :
        Tool names in dispatch order. Empty list if no tools needed.
    write_episode :
        True → EpisodicMemoryWriter runs before the agent call.
    episode_type :
        Type hint for episodic extraction. None when write_episode is False.
    compound :
        True → multiple signal types detected; ControllerAgent sequences
        execution in priority order.
    graph_query :
        (direction, node_id, resolved_stem) when Priority 3c matched a
        structural graph lookup; None otherwise. direction is "incoming"
        or "outgoing". resolved_stem is carried alongside node_id so
        downstream consumers skip a second MemoryManager round-trip just
        to recover the display name.
    tool_signal_source :
        Provenance of tools_to_call, for per-turn auditability: "keyword"
        when a deterministic P3/P3b/compound match populated it,
        "classifier_fallback" when the feature-flagged P6-fallthrough
        tool-need classifier populated it (active mode only),
        "slash_command" when a leading "/chart"/"/research" token (Priority
        0) forced it, or None when tools_to_call is empty or was set by a
        code path that predates this attribution (e.g. P3c never sets
        tools_to_call, so it stays None there too).
    file_op_deferred :
        True → Priority 3 detected a file_op-shaped instruction whose
        content must be generated by the agent before the file can be
        written (e.g. "write a haiku about the sea and save it as
        haiku.md") rather than being supplied literally in the
        instruction (fenced/quoted text, or "with the content"/
        "containing"/"that says" phrasing). "file_op" is deliberately
        NOT added to tools_to_call in this case — there's nothing to
        dispatch yet, since the content doesn't exist at planning time.
        file_op_path/file_op_action carry the pre-resolved destination so
        a future controller step can dispatch file_op once generation
        completes. False for every other RoutingPlan, including file_op
        matches where the content was already present in the instruction
        (those still take the tools_to_call=["file_op"] path unchanged).
    file_op_path :
        Destination path for a deferred file_op, resolved the same way
        _derive_file_op_path() resolves it at dispatch time. None unless
        file_op_deferred is True.
    file_op_action :
        "write" or "append" — the file_op action for a deferred file_op,
        resolved the same way _derive_file_op_action() resolves it at
        dispatch time. None unless file_op_deferred is True.
    diff_target :
        Resolved wiki page stem (e.g. "localist-software-stack") when
        Priority 1b matched a diff-only wiki-update instruction and target
        resolution succeeded. None in every other case, including a P1b
        keyword match whose target resolution failed or was ambiguous
        (that case routes to conversational_agent for clarification
        instead — see Planner._priority1b_diff()). ControllerAgent wires
        this into the wiki_agent SubTask.context as "diff_target".
    """
    agent:          str
    fetch_episodic: bool
    fetch_rag:      bool
    tools_to_call:  list[str]  = field(default_factory=list)
    write_episode:  bool       = False
    episode_type:   str | None = None
    compound:       bool       = False
    priority:       int        = 6
    graph_query:    tuple[str, int, str] | None = None
    tool_signal_source: str | None = None
    file_op_deferred: bool       = False
    file_op_path:     str | None = None
    file_op_action:   str | None = None
    diff_target:      str | None = None


